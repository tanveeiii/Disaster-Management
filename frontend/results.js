const data = JSON.parse(localStorage.getItem("resultData"));

if (!data) {
  alert("No data found!");
  window.location.href = "index.html";
}

// 🎯 Fill values
document.getElementById("risk").innerText = data.risk_level.toUpperCase();
document.getElementById("mag").innerText = data.magnitude + " Richter";
document.getElementById("radius").innerText = data.affected_radius_km + " km";
document.getElementById("rec").innerText = data.recommendation;


// 🎨 Risk Color Logic
let barColor = "#557373";
let badgeColor = "#16a34a";

if (data.risk_level === "High") {
  barColor = "#dc2626";
  badgeColor = "#dc2626";
} else if (data.risk_level === "Medium") {
  barColor = "#f59e0b";
  badgeColor = "#f59e0b";
}

// 🟢 Risk Badge
const badge = document.getElementById("riskBadge");
if (badge) {
  badge.innerText = data.risk_level.toUpperCase() + " RISK";
  badge.style.background = badgeColor;
}


// 🗺️ Map Integration
const lat = data.latitude || 20.5937;
const lon = data.longitude || 78.9629;

const mapFrame = document.getElementById("mapFrame");
if (mapFrame) {
  mapFrame.src = `https://maps.google.com/maps?q=${lat},${lon}&z=5&output=embed`;
}


// 📊 BAR CHART
new Chart(document.getElementById("chart1"), {
  type: "bar",
  data: {
    labels: ["Magnitude", "Impact Radius"],
    datasets: [{
      label: "Earthquake Strength",
      data: [data.magnitude, data.affected_radius_km],
      backgroundColor: [barColor, "#272401"],
      borderRadius: 12
    }]
  },
  options: {
    responsive: true,
    plugins: {
      legend: { display: false }
    },
    animation: {
      duration: 1500,
      easing: "easeOutQuart"
    },
    scales: {
      y: {
        beginAtZero: true
      }
    }
  }
});


// 📈 LINE CHART
new Chart(document.getElementById("chart2"), {
  type: "line",
  data: {
    labels: ["Past 1", "Past 2", "Past 3", "Past 4", "Now"],
    datasets: [{
      label: "Seismic Activity Trend",
      data: data.history,
      borderColor: "#557373",
      backgroundColor: "rgba(85,115,115,0.2)",
      fill: true,
      tension: 0.4,
      pointRadius: 4
    }]
  },
  options: {
    responsive: true,
    animation: {
      duration: 1500
    }
  }
});


// 📄 DOWNLOAD (PRINTABLE PDF STYLE)
document.getElementById("download").onclick = () => {

  const reportWindow = window.open("", "_blank");

  reportWindow.document.write(`
    <html>
    <head>
      <title>Earthquake Report</title>
      <style>
        body { font-family: Arial; padding: 20px; }
        h1 { color: #272401; }
      </style>
    </head>
    <body>
      <h1>Earthquake Analysis Report</h1>
      <hr>

      <p><b>Risk Level:</b> ${data.risk_level}</p>
      <p><b>Magnitude:</b> ${data.magnitude}</p>
      <p><b>Affected Radius:</b> ${data.affected_radius_km} km</p>
      <p><b>Location:</b> ${lat}, ${lon}</p>

      <h3>Recommendation</h3>
      <p>${data.recommendation}</p>

      <br>
      <p style="color:gray;">Generated by Disaster Management System</p>
    </body>
    </html>
  `);

  reportWindow.document.close();
  reportWindow.print();
};